package br.com.abc.introducao;

public class Pacotes {
    // Java é uma linguagem orientada a pacotes.
    // src é o pacote padrao no Java.
    // Nao é recomendavel criarmos classes no pacote padrao(src), é recomendavel criarmos varios outros pacotes e separarmos as classes
    // entre esses pacotes. Os pacotes sao "traduzidos" em diretorios no windows.
    // Os pacotes servem para ajudar que as classes de um pacote nao entrem em conflito com classes de outros pacotes ou projetos.

    // convençao de nomeaçao de pacotes: devemos nomear os pacotes em empresas com o nome inverso ao nome do dominio, por
    // exemplo, se o dominio da empresa é abc.com.br entao devemos nomear o pacote principal como br.com.abc.
    // Outra convençao é que os nome dos pacotes devem ter todas as letras minusculas.

    // Caso uma classe esteja dentro de um pacote a primeira linha dessa classe deverá ser OBRIGATORIAMENTE o nome do pacote(package),
    // exceto para comentarios que podem vir acima do nome do pacote.

}
