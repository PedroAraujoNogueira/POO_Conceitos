// A UML é uma linguagem de modelagem de software. É uma linguagem onde temos uma quantidade de elementos graficos que combinados formam
// diagramas e esses diagramas sao utilizados na fase de planejamento do software.

// Dentre os diagramas da UML alguns dos mais importante sao: diagramas de atividades, diagramas de classes, e o diagrama de sequencia.

// A UML foi definida de forma que ela seja independente de linguagem. Usada para planejamento, analaise e "previsoes" em software.
// Um bom programa pra fazermos modelagens de UML é o Astha Community.

// Uma grande vantagem de usarmos diagramas de classe antes de começarmos a codificar é que podemos prever erros que provavelmente so
// descobririamos no decorrer do programa depois de muitas linhas de codigo.

// No diagrama de classes da UML a classe é representada por um retângulo em pé, no qual é dividido em 3 partes. A primeira parte fica o
// nome da Classe, a segunda parte fica os atributos de classe, a terceira parte ficam os metodos da classe.

// No diagrama de classe o modificador de acesso public é representado pelo sinal de +, e os atributos sao representados com o modificador
// de acesso, seguido pelo nome do atributo, seguido por :, seguido pelo tipo do atributo. Exemplo: + nomeAluno: String

// No diagrama de classe os métodos sao representados com o modificador de acesso, seguido pelo nome do método com parenteses e os respectivos
// parametros, seguido por :, seguido pelo tipo de retorno do metodo. Exemplo: + getNomeAluno(): String. Os parametros sao feitos sao feitos
// da mesma forma que os atributos, exceto pelo modificador de acesso que nao irá existir.

// Geralmente no diagrama de classes nao colocamos os atributos do relacionamento entre classes, pois ja fica implicito atraves da ligaçao
// entre as classes.